package www.arithmetic.leetcode.com.onehundred;

/**
 * @author: huyining
 * @since :    2019-07-25
 */
public class ListNode {

    int val;
    ListNode next;

    ListNode(int x) {
        val = x;
    }

    @Override
    public String toString() {
        return "ListNode{" +
                "val=" + val +
                ", next=" + next +
                '}';
    }
}
